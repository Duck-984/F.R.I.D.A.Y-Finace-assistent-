def get_daily_tip() -> str:
    """Случайный совет."""
    return random.choice(TIPS)


def get_tips_by_count(count: int = 3) -> str:
    """Несколько случайных советов."""
    chosen = random.sample(TIPS, min(count, len(TIPS)))
    lines = ["💡 *Советы по экономии:*", ""]
    for i, tip in enumerate(chosen, 1):
        lines.append(f"{i}. {tip}")
    return "\n".join(lines)
